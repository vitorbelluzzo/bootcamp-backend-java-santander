package CursosintaxeJava.modulos.tiposEVariaveis;

public class tiposVariaveis {
   public static void main(String[] args) {
     // tipos primitivos em java    |  memoria  | valor minimo     | valor maximo
       // a mais comum e utilizada é int  
      int typenumber = 4; //        4 bytes  | -2.147.483.648   | 2.147.483.648  
      byte typebite = 1; //         1 byte   | -128             | 127
      short typeshort = 2;//        2 bytes  | -32.768          | 32.768          
      long typeLong = 8; //         8 bytes  | -9.223.372.036...| 9.223.372.036... 
      // ----------------------------------------------------------------------------
       // a mais comum e utilizada em java é DOUBLE    
      final float typefloat = 4; //        4 bytes  
      double typedouble = 8; //      8 bytes
      
   }
}
